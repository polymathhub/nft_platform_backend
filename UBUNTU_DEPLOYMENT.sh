echo ""
echo "Step 12: Setup Nginx reverse proxy (optional)"
echo "=========================================="
echo "Run this on Ubuntu server:"
echo ""
echo "  sudo apt install -y nginx"
echo "  sudo cp deployment/nginx/nft-platform.conf /etc/nginx/sites-available/"
echo "  sudo ln -s /etc/nginx/sites-available/nft-platform.conf /etc/nginx/sites-enabled/"
echo "  sudo systemctl enable nginx"
echo "  sudo systemctl start nginx"
echo ""
echo "Access your app at: http://5.252.155.82"
